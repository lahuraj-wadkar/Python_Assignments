#***************************************************************************************************
#                  Sum of  lst members                        
#***************************************************************************************************


def Addition(lst):
    sum = 0
    for i in lst:
        sum += i
    print(f"Addition of {lst} is {sum}")


def main():
    lst = [13,5,45,7,4,56]
    Addition(lst)


if(__name__ == "__main__"):
    main()